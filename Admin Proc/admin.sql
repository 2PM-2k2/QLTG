DROP PROC IF EXISTS USP_getAllAccounts
GO

CREATE 
--ALTER 
PROC USP_getAllAccounts
AS
	DECLARE @LIST TABLE(ID CHAR(15), NAME CHAR(50), TYPE CHAR(10))
	INSERT @LIST SELECT AUTHORID, AUTHORNAME, 'AUTHOR' AS TYPE FROM AUTHOR
	INSERT @LIST SELECT EDITORID, EDITORNAME, 'EDITOR' AS TYPE FROM EDITOR
	SELECT * FROM @LIST
GO

