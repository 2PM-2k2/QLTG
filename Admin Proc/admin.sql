GO

CREATE 
--ALTER 
PROC USP_getAdminOverview
AS
	SELECT (SELECT COUNT(*) FROM AUTHOR) AS 'SLTG', (SELECT COUNT(*) FROM EDITOR) AS 'SLBT',
		(SELECT COUNT(*) FROM STORY WHERE STATE = 'West Virginia') AS 'ONPROGRESS', (SELECT COUNT(*) FROM STORY WHERE STATE = 'North Dakota') AS 'DONE'
GO

